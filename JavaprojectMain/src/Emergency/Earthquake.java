public class Earthquake  {
    public String to_do()
    {
        
        String EarthquakeT=
"1. Please stay calm and take cover under a sturdy table or against an interior wall.\n" +
"2. Kindly protect your head and neck and stay away from windows or heavy objects.\n" +
"3. Please wait until the shaking stops before moving to a safe place.\n";
        
        return EarthquakeT;
    }
    
   
    public String not_to_do(){
        
        String EarthquakeNT=
"1. Please do not panic or run during the earthquake.\n" +
"2. Kindly avoid using elevators or standing near glass and tall furniture.\n" +
"3. Please do not rush outside until it is safe to do so.\n";
        
        return EarthquakeNT;
    }
}
